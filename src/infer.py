def main():
    device = 'cuda' if torch.cuda.is_available() else 'cpu'
    checkpoint_path = BEST_MODEL_PATH 
    
    # Colores para la consola
    AZUL_CLARO = "\033[96m"
    VERDE = "\033[92m"
    ROJO = "\033[91m"
    RESET = "\033[0m"

    try:
        model, tokenizer = load_model_and_tokenizer(checkpoint_path, device)
        display_banner()

        while True:
            text_to_translate = input(f" {AZUL_CLARO}‚ĚĮ{RESET} Ingl√©s: ")
            if text_to_translate.lower() in ['exit', 'quit', 'salir']:
                print(f"\nūüĎč ¬°Hasta pronto!{RESET}")
                break
            if not text_to_translate.strip():
                continue

            input_ids = tokenizer.encode(text_to_translate, pad=True)
            x = torch.tensor([input_ids], dtype=torch.long).to(device)

            y_pred = model.predict(
                x=x, 
                bos_id=tokenizer.start_id, 
                end_id=tokenizer.end_id, 
                device=device
            )

            translation = tokenizer.decode(y_pred[0].tolist(), skip_special_tokens=True)
            print(f"   {VERDE}Traducci√≥n:{RESET} {translation}\n")

    except Exception as e:
        print(f"\n {ROJO}‚úĖ Error:{RESET} {e}\n")

if __name__ == "__main__":
    main()